# EX02: Escreva um programa em Python que solicita ao usuário três números  e apresenta a média aritmética dos números informados

nmr1 = int(input("Digite o primeiro número:"))
nmr2 = int(input("Digite o segundo número:"))
nmr3 = int(input("Digite o terceiro número:"))

media_aritmetica = (nmr1 + nmr2 + nmr3)/3

print("A média aritmética dos três números é:", media_aritmetica)